package project.lectureEvaluation;

public class FixedChoiceQuestionnaire {
	public int fixedChoiceQuestionnaireID;
	public String fixedChoiceQuestionTypeID;
	public String fixedChoiceQuestionDivision;
	public String fixedChoiceQuestionnaireContent;
	
	public FixedChoiceQuestionnaire(){}

	public int getFixedChoiceQuestionnaireID() {
		return fixedChoiceQuestionnaireID;
	}

	public void setFixedChoiceQuestionnaireID(int fixedChoiceQuestionnaireID) {
		this.fixedChoiceQuestionnaireID = fixedChoiceQuestionnaireID;
	}

	public String getFixedChoiceQuestionTypeID() {
		return fixedChoiceQuestionTypeID;
	}

	public void setFixedChoiceQuestionTypeID(String fixedChoiceQuestionTypeID) {
		this.fixedChoiceQuestionTypeID = fixedChoiceQuestionTypeID;
	}

	public String getFixedChoiceQuestionDivision() {
		return fixedChoiceQuestionDivision;
	}

	public void setFixedChoiceQuestionDivision(String fixedChoiceQuestionDivision) {
		this.fixedChoiceQuestionDivision = fixedChoiceQuestionDivision;
	}

	public String getFixedChoiceQuestionnaireContent() {
		return fixedChoiceQuestionnaireContent;
	}

	public void setFixedChoiceQuestionnaireContent(String fixedChoiceQuestionnaireContent) {
		this.fixedChoiceQuestionnaireContent = fixedChoiceQuestionnaireContent;
	}
	
	
	
}
